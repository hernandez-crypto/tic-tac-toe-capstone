DROP TABLE IF EXISTS BOARD;
DROP TABLE IF EXISTS users;

CREATE TABLE users (
   id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
   user_name TEXT NOT NULL UNIQUE,
   password TEXT NOT NULL,
   date_created TIMESTAMP DEFAULT now() NOT NULL
);

CREATE TABLE BOARD (
    GAME_ID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    DATE_CREATED TIMESTAMP DEFAULT now() NOT NULL,
    PLAYER_STARTED_ID INTEGER REFERENCES users(id) ON DELETE CASCADE,
    PLAYER_JOINED_ID INTEGER REFERENCES users(id) ON DELETE CASCADE,
    SQUARE_one INTEGER,
    SQUARE_two INTEGER,
    SQUARE_three INTEGER,
    SQUARE_four INTEGER,
    SQUARE_five INTEGER,
    SQUARE_six INTEGER,
    SQUARE_seven INTEGER,
    SQUARE_eight INTEGER,
    SQUARE_nine INTEGER
);